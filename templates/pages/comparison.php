    <h1 class="text-3xl md:text-4xl font-bold mb-6 text-slate-900">Manual J vs. "Rule of Thumb": Why Guessing Costs You
        Money</h1>

    <div class="prose prose-slate max-w-none">
        <p class="lead text-xl text-slate-600 mb-8">
            For decades, contractors used a simple rule: "500 sq ft per ton". In modern homes, this is almost always
            wrong.
        </p>

        <h2 class="text-2xl font-bold text-slate-900 mt-8 mb-4">The Problem with Rules of Thumb</h2>
        <p>
            Old "Rules of Thumb" (like 400-600 sq ft per ton) assume 1970s insulation, single-pane windows, and leaky
            ducts.
            Modern homes are tighter and better insulated. Using these old rules leads to <strong>Oversizing</strong>.
        </p>

        <div class="bg-red-50 border-l-4 border-red-500 p-6 my-8">
            <h3 class="font-bold text-red-900 mb-2">Risks of Oversized AC Units</h3>
            <ul class="list-disc list-inside text-red-800 space-y-1">
                <li><strong>Short Cycling:</strong> Turns on/off rapidly, wearing out parts.</li>
                <li><strong>High Humidity:</strong> Doesn't run long enough to remove moisture (clammy feel).</li>
                <li><strong>Noise:</strong> Moves too much air for the ducts.</li>
                <li><strong>Cost:</strong> Bigger units cost more upfront and use more power starting up.</li>
            </ul>
        </div>

        <h2 class="text-2xl font-bold text-slate-900 mt-8 mb-4">What Manual J Does Differently</h2>
        <p>
            ACCA Manual J calculates load based on physics, not guesses. It considers:
        </p>
        <ul class="grid md:grid-cols-2 gap-4 my-6">
            <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                <strong>Orientation:</strong> West-facing windows gain more heat than North-facing ones.
            </li>
            <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                <strong>Insulation:</strong> R-values of walls, ceilings, and floors.
            </li>
            <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                <strong>Infiltration:</strong> How airtight the house is (ACH).
            </li>
            <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                <strong>Internal Gains:</strong> Heat from people, cooking, and appliances.
            </li>
        </ul>

