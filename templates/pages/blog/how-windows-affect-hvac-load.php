            <h2 id="shgc">3. SHGC (Solar Heat Gain Coefficient)</h2>
            <p>
                When buying windows, you'll see a NFRC sticker with this number. It ranges from 0 to 1. It represents
                the <strong>fraction of solar radiation</strong> admitted through the window.
            </p>
