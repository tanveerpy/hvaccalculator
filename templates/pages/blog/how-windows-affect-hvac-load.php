            <p>
                <strong>Impact:</strong> Changing West-facing windows from Clear (0.76) to Low-E (0.30) cuts the solar
                load by more than half.
            </p>

            <h2 id="ufactor">4. U-Factor vs SHGC</h2>
            <p>
                Don't confuse the two.
            </p>
            <ul>
                <li><strong>U-Factor:</strong> Measures <em>conduction</em> (heat moving through the material because of
                    temperature difference). Important for <strong>Heating</strong> climates and winter comfort.</li>
                <li><strong>SHGC:</strong> Measures <em>radiation</em> (heat from the sun). Critical for
                    <strong>Cooling</strong> climates.</li>
            </ul>

            <h2 id="solution">5. The Blind Spot (Literally)</h2>
            <p>
                There is a common misconception that closing internal white blinds stops the heat.
            </p>
            <p>
                <strong>It doesn't.</strong> By the time the sunlight hits your blinds, the heat is already inside the
                glass. The blinds absorb the heat and radiate it into the room.
            </p>
            <p>
                The only ways to stop solar heat are:
            </p>
            <ol>
                <li><strong>External Shading:</strong> Awnings, trees, or solar screens (stops heat <em>before</em> the
                    glass).</li>
                <li><strong>Low-E Glass:</strong> Reflects invisible infrared heat while letting visible light in.</li>
            </ol>

