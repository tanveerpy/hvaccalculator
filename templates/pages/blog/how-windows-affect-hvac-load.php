            <p class="lead text-xl text-slate-700 font-medium">
                Windows are usually the weakest link in your home's thermal envelope. While an insulated wall stops ~93%
                of heat, a standard window might only stop 30-50%.
            </p>

            <h2 id="orientation">1. Why Orientation is Critical</h2>
            <p>
                In the Northern Hemisphere, the path of the sun largely determines your cooling load.
            </p>
            <ul>
                <li><strong>North Windows:</strong> Receive almost no direct sunlight. They only deal with ambient heat
                    (conduction).</li>
                <li><strong>South Windows:</strong> The sun is high in the sky in summer, often blocked by roof
                    overhangs. In winter, the sun is lower, providing free heat.</li>
                <li><strong>East Windows:</strong> Get morning sun. Significant heat gain, but usually when the outdoor
                    air is cooler.</li>
                <li><strong>West Windows ( The Danger Zone):</strong> The late afternoon sun hits these at a low angle,
                    blasting perpendicular rays through the glass right when the outdoor temperature is hottest (4 PM -
                    6 PM).</li>
            </ul>

