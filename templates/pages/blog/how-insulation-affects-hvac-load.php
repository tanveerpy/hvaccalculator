            <p class="lead text-xl text-slate-700 font-medium">
                Insulation acts as a barrier to heat flow. In the summer, it keeps heat out. In the winter, it keeps
                heat in. The better your insulation, the less work your HVAC system has to do.
            </p>

            <h2 id="basics">The Basics of Heat Flow</h2>
            <p>
                Thermodynamics dictates that heat always moves from hot to cold. Your AC is essentially a pump moving
                that heat back outside.
            </p>
            <p>
                If your house is a bucket and heat is water leaking in, your AC is the bailer. You can buy a bigger
                bailer (AC), or you can plug the holes (Insulation). Plugging the holes is always cheaper.
            </p>

            <h2 id="r-value">Understanding R-Value</h2>
            <p>
                <strong>R-Value</strong> measures thermal resistance. Higher numbers mean better insulation.
            </p>
