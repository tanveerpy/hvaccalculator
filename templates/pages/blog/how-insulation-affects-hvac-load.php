            <h2 id="money">Where to Spend Your Money</h2>
            <p>
                If you have an uninsulated attic (R-0), typically ~40% of your cooling load comes from the ceiling.
                Adding R-30 insulation can cut that load by 90%, potentially allowing you to drop your AC size by a full
                ton.
            </p>
            <p>
                Before buying a new unit, ask your contractor: <em>"If I spent $1,000 on attic insulation, could I buy a
                    smaller AC?"</em>
            </p>

