            </p>

            <h2 id="diy">1. When to DIY (Estimation)</h2>
            <p>
                You should run your own calculation if:
            </p>
            <ul>
                <li><strong>Fact Checking:</strong> You got a quote for a 5-ton unit and you suspect it's too big. (Our
                    tool will likely show you only need 3 tons).</li>
                <li><strong>Budgeting:</strong> You are planning a renovation and want to know broadly what size unit
                    you'll need.</li>
                <li><strong>Curiosity:</strong> You want to understand your home's efficiency.</li>
            </ul>
            <p>
                <em>Accuracy:</em> A careful DIY calculation is usually within 5-10% of a professional engineering
                report.
            </p>

            <h2 id="pro">2. When to Hire a Pro (Certification)</h2>
            <p>
                You <strong>must</strong> hire a licensed Mechanical Engineer or certified HVAC designer if:
            </p>
            <ul>
                <li><strong>Permitting:</strong> Most building departments require a full Manual J report (often
                    accompanied by Manual S and D) to issue a permit for new construction.</li>
                <li><strong>Zoning:</strong> If you are designing a complex zoned system with dampers.</li>
                <li><strong>Litigation:</strong> If you are in a legal dispute with a builder about an improper
                    installation.</li>
            </ul>

            <h2 id="questions">3. 3 Questions to Ask Your Contractor</h2>
            <div class="bg-gray-50 border border-gray-200 rounded-lg p-6 my-6">
                <p class="font-bold text-slate-900">1. "Will you perform a Manual J calculation?"</p>
                <p class="mb-4 text-sm text-slate-600">If they say "No, I've been doing this 30 years, I know what you
                    need," walk away.</p>

                <p class="font-bold text-slate-900">2. "Can I see the report?"</p>
                <p class="mb-4 text-sm text-slate-600">A real report is a multi-page PDF from software like Wrightsoft
                    or Elite, showing inputs for every window and wall.</p>

                <p class="font-bold text-slate-900">3. "What design temperatures are you using?"</p>
                <p class="text-sm text-slate-600">Make sure they aren't "padding" the stats (e.g., using 105°F outdoor
                    temp when the manual says 95°F) just to justify a bigger unit.</p>
            </div>

