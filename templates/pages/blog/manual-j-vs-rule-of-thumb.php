            <h2 id="what-manual-j-does">What Manual J Does Differently</h2>
            <p>
                ACCA Manual J calculates load based on physics, not guesses. It breaks the house down into its
                thermodynamic components:
            </p>
            <ul class="grid md:grid-cols-2 gap-4 my-6 not-prose">
                <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                    <strong>Orientation:</strong> West-facing windows gain massive amounts of heat in the afternoon.
                    Manual J accounts for this; Rule of Thumb ignores it.
                </li>
                <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                    <strong>Insulation:</strong> An R-30 attic resists heat flow far better than an R-11 attic. Manual J
                    calculates the exact difference in BTU transfer.
                </li>
                <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                    <strong>Infiltration:</strong> Manual J uses "Air Changes per Hour" (ACH) to determine how much
                    outdoor air is entering the home.
                </li>
                <li class="bg-white p-4 border border-slate-200 rounded shadow-sm">
                    <strong>Internal Gains:</strong> It adds specific loads for the number of occupants (biological
                    heat) and appliances (kitchen/laundry).
                </li>
            </ul>

