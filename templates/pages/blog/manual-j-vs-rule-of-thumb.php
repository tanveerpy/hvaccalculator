            <p class="lead text-xl text-slate-700 font-medium">
                For decades, air conditioning contractors used a simple rule: "500 square feet per ton". If you had a
                2,000 square foot house, you got a 4-ton unit. Case closed.
            </p>
            <p>
                In 1970, that might have been close enough. But in modern homes with double-pane windows and actual
                insulation, this rule is <strong>almost always wrong</strong>. It is a relic of a time when energy was
                cheap and building science didn't exist.
            </p>

            <h2 id="the-problem">The Problem with Rules of Thumb</h2>
            <p>
                Old "Rules of Thumb" (typically varying from 400 to 600 sq ft per ton) rely on assumptions that are no
                longer true:
            </p>
            <ul>
                <li><strong>Assumption:</strong> "Houses have no insulation." (False: Modern codes require R-13 to R-20
                    walls.)</li>
                <li><strong>Assumption:</strong> "Windows are single-pane glass." (False: Low-E double panes are
                    standard.)</li>
                <li><strong>Assumption:</strong> "Houses leak air everywhere." (False: House wrap and better sealing
                    make homes tight.)</li>
            </ul>
            <p>
                When a contractor uses these outdated metrics on a modern home, they inevitably
                <strong>oversize</strong> the system.
            </p>

