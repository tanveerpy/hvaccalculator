            <h2 id="risks">The 4 Major Risks</h2>

            <h3>1. The "Cave Effect" (High Humidity)</h3>
            <p>
                It takes about 10-15 minutes of run time for the evaporator coil inside your unit to get cold enough to
                start condensing water out of the air. If the unit satisfies the thermostat in 5 minutes, <strong>zero
                    dehumidification</strong> happens.
            </p>
            <p>
                The result is a house that is 70°F but feels damp, sticky, and clammy—like a cold cave.
            </p>

            <h3>2. Mold Growth</h3>
            <p>
                High humidity in your ducts and walls is the primary recipe for mold. By oversizing your unit, you are
                creating a biological factory in your attic.
            </p>

            <h3>3. Equipment Suicide</h3>
            <p>
                Imagine stuck in bumper-to-bumper traffic—start, stop, start, stop. It destroys your car's brakes and
                transmission.
            </p>
            <p>
                Compressors are the same. Most wear happens during startup (in-rush current). A properly sized unit runs
                for long, steady cycles (highway driving). An oversized unit starts and stops 6-8 times an hour (city
                traffic). It <strong>will</strong> die years earlier.
            </p>

            <h3>4. Higher Energy Bills</h3>
            <p>
                Starting an electric motor takes a massive surge of electricity (Locked Rotor Amps). Doing this
                constantly drives up your electric bill significantly.
            </p>

