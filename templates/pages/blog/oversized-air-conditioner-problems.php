            <h2 id="myth">The "Bigger is Better" Myth</h2>
            <p>
                In many aspects of life (TVs, bank accounts, pizza), bigger is indeed better. But HVAC is more like a
                shoe: it needs to fit <em>perfectly</em>. Wearing a size 14 shoe when you are a size 10 doesn't give you
                "more room"; it makes you stumble.
            </p>

            <h2 id="two-jobs">The Two Jobs of an AC</h2>
            <p>
                To understand why sizing matters, you must realize an air conditioner has two equally important jobs:
            </p>
            <ol>
                <li><strong>Sensible Cooling:</strong> Lowering the temperature (what the thermostat reads).</li>
                <li><strong>Latent Cooling:</strong> Removing liquid water from the air (humidity control).</li>
            </ol>
            <p>
                If the unit is too big, it does job #1 too fast and completely fails at job #2.
            </p>

