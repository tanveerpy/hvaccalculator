            <h2 id="what-is-btu">1. What is a BTU?</h2>
            <p>
                <strong>BTU</strong> stands for <em>British Thermal Unit</em>. It is a measure of heat energy. One BTU
                is roughly the amount of energy released by burning a single wooden match.
            </p>
            <p>
                In HVAC, we talk about BTUs <em>per hour</em> (BTU/h). This is the rate at which your air conditioner
                removes heat from your house (or your furnace adds it).
            </p>

            <h2 id="why-estimations-fail">2. Why "BTU per Square Foot" is Wrong</h2>
            <p>
                Many online charts suggest "20-30 BTUs per square foot". This is a dangerous oversimplification.
            </p>
            <p>
                Two houses with the same square footage can have drastically different loads because of:
            </p>
            <ul>
                <li><strong>Ceiling Height:</strong> A 10ft ceiling holds 25% more air volume than an 8ft ceiling.</li>
                <li><strong>Window Exposure:</strong> A house with a wall of west-facing windows behaves like a
                    greenhouse.</li>
                <li><strong>Insulation:</strong> Poor insulation lets heat pass through walls as if they weren't there.
                </li>
            </ul>

