            <h2 id="why-it-matters">Why Size Matters: The Triple Threat</h2>
            <p>
                The goal of Manual J is to find the "Goldilocks" size for your HVAC system—not too big, not too small.
            </p>
            <h3>1. Avoid Oversizing</h3>
            <p>
                An oversized unit cools the room too quickly and shuts off. This "short cycling" wastes energy and fails
                to remove humidity, leading to a cold, clammy house.
            </p>
            <h3>2. Avoid Undersizing</h3>
            <p>
                An undersized unit will run constantly on the hottest days and still fail to keep the house cool.
            </p>
            <h3>3. Save Money</h3>
            <p>
                By installing the correct size (often smaller than what a contractor "guesses"), you save money on the
                equipment upfront and on monthly utility bills.
            </p>

            <h2 id="comparison">Manual J vs. Square Footage</h2>
            <p>
                Use this simple rule: <strong>If your contractor sizes your AC based solely on square footage, fire
                    them.</strong>
            </p>
            <p>
                A 2,000 sq ft home built in 1950 needs vastly more cooling than a 2,000 sq ft home built in 2024.
                Treating them the same is malpractice that results in discomfort and wasted money.
            </p>

