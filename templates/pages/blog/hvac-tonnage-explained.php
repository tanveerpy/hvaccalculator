            <h2 id="history">1. The History (Blocks of Ice)</h2>
            <p>
                Before modern air conditioning, buildings were cooled using massive blocks of ice harvested from frozen
                lakes. When mechanical refrigeration was invented, people needed a way to compare it to what they knew:
                ice.
            </p>
            <p>
                Engineers calculated how much heat energy was required to melt exactly <strong>2,000 lbs (1 ton) of
                    ice</strong> over a 24-hour period.
            </p>

            <h2 id="math">2. The Math (BTUs to Tons)</h2>
            <p>
                A BTU (British Thermal Unit) is the amount of heat needed to raise 1 lb of water by 1Â°F.
            </p>
            <ul>
                <li>To melt 1 lb of ice requires 144 BTUs (Latent Heat of Fusion).</li>
                <li>2,000 lbs (1 ton) x 144 BTUs = 288,000 BTUs per day.</li>
                <li>288,000 BTUs / 24 hours = <strong>12,000 BTUs per hour.</strong></li>
            </ul>
            <div
                class="bg-blue-50 p-6 rounded-lg text-center font-bold text-blue-900 text-xl border border-blue-200 my-8">
                1 Ton = 12,000 BTU/h
            </div>

