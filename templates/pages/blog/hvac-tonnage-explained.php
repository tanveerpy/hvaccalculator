            <h2 id="choosing">4. Choosing the Right Tonnage</h2>
            <p>
                If your calculation comes out to 29,000 BTUs, you are between sizes (2.5 tons is 30,000 BTUs, 2.0 tons
                is 24,000 BTUs).
            </p>
            <p>
                In general, it is better to slightly undersize than oversized (to preserve dehumidification), or better
                yet, choose a <strong>variable speed inverter unit</strong> that can ramp up or down to match the exact
                load.
            </p>

